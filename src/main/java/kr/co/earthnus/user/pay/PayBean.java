package kr.co.earthnus.user.pay;

public class PayBean {
	private int pay_no;
	private String pay_num;
	private String pay_id;
	private String pay_name;
	private String pay_dnum;
	private int pay_price;
	private int pay_point;
	private String pay_date;
	
	public int getPay_no() {
		return pay_no;
	}
	public void setPay_no(int pay_no) {
		this.pay_no = pay_no;
	}
	public String getPay_num() {
		return pay_num;
	}
	public void setPay_num(String pay_num) {
		this.pay_num = pay_num;
	}
	public String getPay_id() {
		return pay_id;
	}
	public void setPay_id(String pay_id) {
		this.pay_id = pay_id;
	}
	public String getPay_name() {
		return pay_name;
	}
	public void setPay_name(String pay_name) {
		this.pay_name = pay_name;
	}
	public String getPay_dnum() {
		return pay_dnum;
	}
	public void setPay_dnum(String pay_dnum) {
		this.pay_dnum = pay_dnum;
	}
	public int getPay_price() {
		return pay_price;
	}
	public void setPay_price(int pay_price) {
		this.pay_price = pay_price;
	}
	public int getPay_point() {
		return pay_point;
	}
	public void setPay_point(int pay_point) {
		this.pay_point = pay_point;
	}
	public String getPay_date() {
		return pay_date;
	}
	public void setPay_date(String pay_date) {
		this.pay_date = pay_date;
	}
}
